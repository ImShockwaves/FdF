#include "libft.h"

int		ft_str_is_alpha(const char *s)
{
	while (s[0] != '\0')
	{
		if (!((s[0] >= 'a' && s[0] <= 'z') || (s[0] >= 'A' && s[0] <= 'Z')))
			return (0);
		s++;
	}
	return (1);
}
